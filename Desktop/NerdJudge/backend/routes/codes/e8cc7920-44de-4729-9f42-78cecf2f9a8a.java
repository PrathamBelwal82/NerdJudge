public class AddTwoNumbers {
    public static void main(String[] args) {
        // Declare variables
        int number1 = 6;
        int number2 = 10;

        // Calculate the sum
        int sum = number1 + number2;

        // Print the result
        System.out.println("The sum of " + number1 + " and " + number2 + " is " + sum);
    }
}
